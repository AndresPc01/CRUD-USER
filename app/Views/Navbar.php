<script>
document.addEventListener("DOMContentLoaded", function() {
    // Recuperar el estado almacenado en localStorage
    const navbars = document.querySelectorAll('.collapse');
    navbars.forEach(navbar => {
        const navbarID = navbar.getAttribute('id');
        const savedState = localStorage.getItem(navbarID);

        if (savedState === 'true') {
            navbar.classList.add('show');
        } else {
            navbar.classList.remove('show');
        }

        navbar.addEventListener('shown.bs.collapse', function() {
            localStorage.setItem(navbarID, 'true');
        });

        navbar.addEventListener('hidden.bs.collapse', function() {
            localStorage.setItem(navbarID, 'false');
        });
    });
});
